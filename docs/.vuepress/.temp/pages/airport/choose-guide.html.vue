<ol>
<li>检查系统时间是否同代理协议对时间精度敏感，偏差超过 2 分钟可能导致连接失败。</li>
<li>检查是否同时开启了其他代理工具 多个代理工具冲突是常见原因。</li>
<li>尝试更新订阅链接 订阅 URL 可能已失效。</li>
<li>以上都正常则联系机场客服。</li>
</ol>
<h3 id="q-使用机场会泄露隐私吗" tabindex="-1"><a class="header-anchor" href="#q-使用机场会泄露隐私吗"><span>Q: 使用机场会泄露隐私吗?</span></a></h3>
<p>理论上机场运营者可以看到您的访问域名（但在 HTTPS 加密下无法看到具体内容）。因此：</p>
<ul>
<li><strong>不建</strong>使用不明来源的免费机场进行敏感操作（如银行转账）。</li>
<li><strong>推荐</strong>选择有信誉的运营 2 年以上的大型机场。</li>
</ul>
<h3 id="q-代理后网速反而变慢了怎么" tabindex="-1"><a class="header-anchor" href="#q-代理后网速反而变慢了怎么"><span>Q: 代理后网速反而变慢了怎么</span></a></h3>
<p>可能的原因：</p>
<ol>
<li>开启了&quot;全局模式&quot;导致国内流量也绕路国切换规则模式&quot;。</li>
<li>当前节点负载过高 切换到延迟更低的节点。</li>
<li>节点倍率过高 (x3.0)，流量消耗快 切换低倍率节点。</li>
</ol>
