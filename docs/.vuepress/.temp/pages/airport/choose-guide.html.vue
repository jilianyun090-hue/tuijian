<p>找一个闲时（如下2-4 点），挑选几个常用节点进行深度测试：</p>
<p><strong>Speedtest 测</strong></p>
<p>打开 <a href="https://www.speedtest.net" target="_blank" rel="noopener noreferrer">Speedtest.net</a>，测试下载速度。对100 Mbps 宽带，代理后能跑80% (80 Mbps) 即为优秀。</p>
<p><strong>YouTube 4K 压力测试</strong></p>
<ol>
<li>打开 YouTube 搜索 4K 视频。</li>
<li>右键视频画面 选择&quot;统计信息 (Stats for nerds)&quot;。</li>
<li>观察 <code v-pre>Connection Speed</code> <code v-pre>Buffer Health</code> 两项数据。</li>
<li>如果能流畅拖动进度条，且 Buffer 始终保持10 秒以上，说明带宽充足。</li>
</ol>
<p><strong>流媒体解锁测</strong></p>
<p>如果您使Netflix、Disney+，尝试播放非自制剧（如《绝命毒师》）。能正常观看说明该节点支持解锁。如果仅能播Netflix 自制剧，说明解锁不完全。</p>
